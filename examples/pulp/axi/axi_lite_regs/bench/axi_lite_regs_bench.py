"""Hand-authored Wave C-2 testbench for the flat-port AXI-Lite register file.

Originally generated by:

    uv run veriforge generate-python-testbench \\
      -f examples/pulp/axi/axi_lite_regs/rtl_flat/axi_lite_regs_flat.sv \\
      --module axi_lite_regs_flat --enhanced --style bench --auto-deps --no-strict \\
      -o examples/pulp/axi/axi_lite_regs/bench/axi_lite_regs_bench.py

The generated scaffold's ``axi_lite_s_axi`` stub was replaced with a real
register sweep (write four words, read them back, byte-strobe partial
write).

Plan summary:
  TestbenchPlan(top='axi_lite_regs_flat')
    domains:
      - clk_i: clock=clk_i (posedge, period=?); reset=rst_ni (active-low, async)
    interfaces:
      - s_axi (axi_lite, role=slave) -> domain=clk_i [structural]
"""

from __future__ import annotations

import argparse
from pathlib import Path

from veriforge.project import parse_file
from veriforge.sim.bench import PlannerOverrides, Testbench

DUT_PATH = Path(__file__).resolve().parents[1] / "rtl_flat" / "axi_lite_regs_flat.sv"


def parse_dut():
    """Parse the DUT module from disk and return ``(design, top_module)``."""
    design = parse_file(DUT_PATH)
    return design, design.get_module("axi_lite_regs_flat")


def build_bench() -> Testbench:
    """Construct the multi-domain Testbench from the parsed DUT."""
    design, dut = parse_dut()
    overrides = PlannerOverrides(iface_domains={"s_axi": "clk_i"})
    return Testbench(dut, design=design, overrides=overrides, engine="reference")


def exercise_s_axi(bench: Testbench) -> None:
    """Write/read sweep against the four 32-bit registers, plus a byte-strobe write."""
    iface = bench.iface("s_axi")

    payload = [0xDEADBEEF, 0xCAFEF00D, 0x1234_5678, 0xA5A5_5A5A]
    for offset, value in zip([0x0, 0x4, 0x8, 0xC], payload, strict=True):
        iface.write(offset, value)

    for offset, expected in zip([0x0, 0x4, 0x8, 0xC], payload, strict=True):
        got = iface.read(offset)
        if got != expected:
            raise AssertionError(f"reg[0x{offset:x}] mismatch: expected 0x{expected:08x}, got 0x{got:08x}")

    # Byte-strobe partial write: only update the low byte of reg[0].
    iface.write(0x0, 0x0000_00FF, strb=0b0001)
    got = iface.read(0x0)
    expected = (payload[0] & 0xFFFF_FF00) | 0xFF
    if got != expected:
        raise AssertionError(f"strb partial write failed: expected 0x{expected:08x}, got 0x{got:08x}")

    print("axi_lite_regs_flat passed: 4-reg sweep + WSTRB partial write")


def run_smoke_test() -> None:
    """Drive the AXI-Lite register file through a full read/write sweep."""
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument(
        "--vcd",
        type=Path,
        default=None,
        help="Optional VCD output path. Captures every top-level DUT signal.",
    )
    args = parser.parse_args()

    bench = build_bench()
    print("Discovered testbench plan:\n")
    print(bench.plan.summary())
    print()

    with bench.run(vcd=args.vcd):
        if args.vcd is not None:
            print(f"VCD tracing -> {args.vcd}\n")
        bench.reset_all()
        exercise_s_axi(bench)


if __name__ == "__main__":
    run_smoke_test()
