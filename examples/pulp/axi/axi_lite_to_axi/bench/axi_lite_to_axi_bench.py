"""Hand-authored Wave D-3 testbench for the pulp axi_lite_to_axi bridge.

Originally generated by:

    uv run veriforge generate-python-testbench \\
      -f examples/pulp/axi/axi_lite_to_axi/tb/axi_lite_to_axi_tb.sv \\
      --module axi_lite_to_axi_exec_tb --enhanced --style bench --no-strict \\
      -o examples/pulp/axi/axi_lite_to_axi/bench/axi_lite_to_axi_bench.py

The pulp bridge is a combinational AXI-Lite -> AXI4 single-beat
adapter. The TB top exposes:

* ``slv`` — AXI-Lite slave (bench drives, role='slave' in AXI parlance,
  i.e. DUT-slave, bench-master).
* ``mst`` — AXI-Lite master (DUT drives, bench responds; the underlying
  bus is single-beat AXI4 but the detector lands it as AXI-Lite because
  the TB ports omit ``awlen``/``arlen``/``rlast``).

We exercise a write/read sweep against the bridge: each AXI-Lite write
on ``slv`` propagates as a single-beat AXI4-style write on ``mst``, the
responder accepts it and stores it in memory; subsequent AXI-Lite reads
on ``slv`` propagate as AXI4-style reads on ``mst`` and the responder
returns the previously-stored value.

Plan summary:
  TestbenchPlan(top='axi_lite_to_axi_exec_tb')
    domains:
      - clk: clock=clk (posedge); reset=rst_n (active-low, async)
    interfaces:
      - slv (axi_lite, role=slave) -> domain=clk
      - mst (axi_lite, role=master) -> domain=clk
"""

from __future__ import annotations

import argparse
from pathlib import Path

from veriforge.project import parse_files
from veriforge.sim.bench import PlannerOverrides, Testbench

SCRIPT_DIR = Path(__file__).resolve().parent
EX_ROOT = SCRIPT_DIR.parent
RTL_DIR = EX_ROOT / "rtl"
TB_FILE = EX_ROOT / "tb" / "axi_lite_to_axi_tb.sv"
FILES = [
    str(RTL_DIR / "axi_pkg.sv"),
    str(RTL_DIR / "axi_lite_to_axi.sv"),
    str(TB_FILE),
]


def parse_dut():
    design = parse_files(
        FILES,
        preprocess=True,
        cache_dir=SCRIPT_DIR / "_vtc_axi_lite_to_axi_pcache",
    )
    return design, design.get_module("axi_lite_to_axi_exec_tb")


def build_bench() -> Testbench:
    design, dut = parse_dut()
    overrides = PlannerOverrides(iface_domains={"slv": "clk", "mst": "clk"})
    return Testbench(dut, design=design, overrides=overrides, engine="reference")


def exercise_bridge(bench: Testbench) -> None:
    """Drive AXI-Lite slave; expect responder-side memory to mirror writes."""
    slv = bench.iface("slv")
    mst = bench.iface("mst")

    # Write/read sweep through 8 word addresses.
    payload = {addr: 0xCAFE_0000 | addr for addr in (0x000, 0x004, 0x010, 0x040, 0x080, 0x100, 0x200, 0x3FC)}

    for addr, value in payload.items():
        slv.write(addr, value)

    # Responder memory should have observed every write.
    for addr, value in payload.items():
        observed = mst.memory.get(addr)
        if observed != value:
            raise AssertionError(f"responder memory mismatch at 0x{addr:03x}: expected 0x{value:08x}, got {observed!r}")

    # Read back through the bridge.
    for addr, value in payload.items():
        got = slv.read(addr)
        if got != value:
            raise AssertionError(f"slv read mismatch at 0x{addr:03x}: expected 0x{value:08x}, got 0x{got:08x}")

    # Write log should have one entry per beat we issued.
    if len(mst.write_log) != len(payload):
        raise AssertionError(f"unexpected write_log length: {len(mst.write_log)} (expected {len(payload)})")
    if len(mst.read_log) != len(payload):
        raise AssertionError(f"unexpected read_log length: {len(mst.read_log)} (expected {len(payload)})")

    print("axi_lite_to_axi passed: 8-word write/read sweep through bridge with responder echo")


def run_smoke_test() -> None:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--vcd", type=Path, default=None, help="Optional VCD output path.")
    args = parser.parse_args()

    bench = build_bench()
    print("Discovered testbench plan:\n")
    print(bench.plan.summary())
    print()

    with bench.run(vcd=args.vcd):
        if args.vcd is not None:
            print(f"VCD tracing -> {args.vcd}\n")
        bench.reset_all()
        exercise_bridge(bench)


if __name__ == "__main__":
    run_smoke_test()
