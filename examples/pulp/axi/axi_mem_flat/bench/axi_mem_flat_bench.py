"""Hand-authored Wave C-3 testbench for the flat-port AXI4 memory.

Originally generated by:

    uv run veriforge generate-python-testbench \\
      -f examples/pulp/axi/axi_mem_flat/rtl_flat/axi_mem_flat.sv \\
      --module axi_mem_flat --enhanced --style bench --auto-deps --no-strict \\
      -o examples/pulp/axi/axi_mem_flat/bench/axi_mem_flat_bench.py

The generated scaffold's ``axi4_s_axi`` stub was replaced with a real
burst exercise: single-beat write/read sweep across all 16 words, an
INCR burst write of 4 beats followed by an INCR burst read back, and a
WSTRB partial-byte write check.

Plan summary:
  TestbenchPlan(top='axi_mem_flat')
    domains:
      - clk_i: clock=clk_i (posedge, period=?); reset=rst_ni (active-low, async)
    interfaces:
      - s_axi (axi4, role=slave) -> domain=clk_i [structural]
"""

from __future__ import annotations

import argparse
from pathlib import Path

from veriforge.project import parse_file
from veriforge.sim.bench import PlannerOverrides, Testbench

DUT_PATH = Path(__file__).resolve().parents[1] / "rtl_flat" / "axi_mem_flat.sv"


def parse_dut():
    """Parse the DUT module from disk and return ``(design, top_module)``."""
    design = parse_file(DUT_PATH)
    return design, design.get_module("axi_mem_flat")


def build_bench() -> Testbench:
    """Construct the multi-domain Testbench from the parsed DUT."""
    design, dut = parse_dut()
    overrides = PlannerOverrides(iface_domains={"s_axi": "clk_i"})
    return Testbench(dut, design=design, overrides=overrides, engine="reference")


def exercise_s_axi(bench: Testbench) -> None:
    """Single-beat sweep + 4-beat INCR burst + WSTRB partial-byte write."""
    iface = bench.iface("s_axi")

    # 1) Single-beat write/read sweep across all 16 words.
    payload = [0xDEAD_0000 | (i & 0xFFFF) for i in range(16)]
    for i, value in enumerate(payload):
        iface.write(i * 4, value)
    for i, expected in enumerate(payload):
        beats = iface.read(i * 4, length=1)
        if beats[0] != expected:
            raise AssertionError(f"single-beat mismatch at word {i}: expected 0x{expected:08x}, got 0x{beats[0]:08x}")

    # 2) Four-beat INCR burst write + read.
    burst_addr = 0x0
    burst = [0xCAFE_F00D, 0x1234_5678, 0xA5A5_5A5A, 0x0BAD_F00D]
    iface.write(burst_addr, burst)
    got = iface.read(burst_addr, length=len(burst))
    if got != burst:
        raise AssertionError(f"burst read mismatch: expected {[hex(v) for v in burst]}, got {[hex(v) for v in got]}")

    # 3) WSTRB partial-byte write: only update the low byte of word 0.
    iface.write(0x0, 0x0000_00FF, strb=0b0001)
    got = iface.read(0x0, length=1)
    expected = (burst[0] & 0xFFFF_FF00) | 0xFF
    if got[0] != expected:
        raise AssertionError(f"strb partial write failed: expected 0x{expected:08x}, got 0x{got[0]:08x}")

    print("axi_mem_flat passed: 16-word sweep + 4-beat INCR burst + WSTRB partial write")


def run_smoke_test() -> None:
    """Drive the AXI4 memory through a burst-capable read/write exercise."""
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument(
        "--vcd",
        type=Path,
        default=None,
        help="Optional VCD output path. Captures every top-level DUT signal.",
    )
    args = parser.parse_args()

    bench = build_bench()
    print("Discovered testbench plan:\n")
    print(bench.plan.summary())
    print()

    with bench.run(vcd=args.vcd):
        if args.vcd is not None:
            print(f"VCD tracing -> {args.vcd}\n")
        bench.reset_all()
        exercise_s_axi(bench)


if __name__ == "__main__":
    run_smoke_test()
